"""章节正文「反八股 / 控水分」约束块 — 供主工作流与 DAG 等共用。

解决模型常见恶习：「不是…而是…」纠正式排比、破折号叠比喻、
同一段感受换三种说法、为凑字数重复描写同一致震撼等。
"""
from __future__ import annotations

from typing import Optional


def build_prose_discipline_block(
    *,
    beat_mode: bool = False,
    beat_target_words: Optional[int] = None,
) -> str:
    """组装注入 system 的戒律段落（中文）。"""
    lines = [
        "━━━ 行文戒律（反八股 / 控水分）━━━",
        "",
        "**纠正式对照（全章禁止）**：禁止在同一句或紧挨两句里用「否定甲 + 肯定乙」的说教对照（含「不是…而是…」「从来不是…」「要的不是…是…」「并非…乃…」及同义变体）。想转折就拆成两句短叙，或直接写动作与结果。",
        "**破折号**：用「——」拖尾补充、叠比喻或改述的句子全章最多 3 句；禁止「事实句——体感比喻——再换说法强调」三连灌水。",
        "**信息**：大约每 300～500 字至少出现一条可复述的新事实（人物立场、线索、因果、决策、关系变化之一），禁止只换手改比喻形容同一件事。",
        "**情节密度（段落级）**：以空行分段为准，每一段内须至少含一项——具体动作并带轻微后果、有信息量的对白、发现/误会/决定之一落地、或可见的空间位移/关系试探；禁止连续两段只有景物/气味/光色铺陈而无人物动作、对白或取舍。",
        "**形容词预算**：同一句中连续修饰词不得超过 2 个；同一段内颜色、气味、质感、温度、抽象氛围词合计超过 4 处时，必须删减到只保留能改变人物判断或行动的细节。",
        "**纯氛围段封杀**：凡是只写夜色、雨声、光影、气味、衣料、建筑质感、内心波动，却没有动作、对白、发现、决定、位移或关系变化的段落，直接改写为事件段。",
        "**设定负担**：新名词、组织名、规则名、病症、材料、官职、法术名不得连续抛出；每引入 1 个新名词，必须绑定一个当场可见的用途、代价、威胁或人物选择。",
        "**开头名词密度**：前 800 字专有名词最多 6 个；优先保留主角、目标资源、倒计时、当前敌人/压力源。设备型号、组织层级、体系分类、历史名词延后。",
        "**纠正式句式预算**：「不是……是……」「并非……而是……」「不是因为……是因为……」及同类结构全章最多 6 次；能直接写动作、事实、后果，就不要先否定再解释。",
        "**爽点反馈链**：大爽点必须有「爆发 -> 他人反应 -> 局势变化 -> 主角新筹码」。反馈不能只写震惊，要落成收枪、让路、改称呼、交易、欠人情、暴露身份、获得谈判资格等实际变化。",
        "**视角**：POV 体感描写少而准即可；同一情绪禁止用两条以上不同比喻接力渲染；设定交代优先对白、动作或一句平叙，忌长串内心排比。",
        "**扩写**：需要加长时，加有信息的对话轮次、可见动作链、带后果的选择；禁止复制粘贴式「众人震惊」排比，或重复描写同一条「电流/暖流/针刺」体感。",
        # ── 新增：体感比喻灌水（最常见的隐性八股）──
        "**体感比喻（高频灌水禁区）**：同一段里「像电流」「像烈火」「像针刺」「暖流涌过」「寒意蔓延」等体感比喻最多出现 1 次；描写同一人物同一情绪的体感句，全段不得超过 2 句；多余的用具体行为（手抖、嘴角微扯、脚步顿住）替换。",
        "**重复震撼/感叹（禁止叠栈）**：「不敢置信」「难以置信」「心跳骤停」「脑袋一片空白」等高惊觉词汇，每章最多各出现 1 次；旁观者震惊反应写 1 条有名有姓的即可，禁止「众人皆惊」「所有人都」「全场哗然」等无名群体句连续出现超过 2 次。",
        "**开场/过渡八股（禁止模板起笔）**：段落开头禁止用「就在此时」「与此同时」「话音刚落」「就在这时」「然而」单独成句充当转场；禁止以「这一刻，他明白了……」「那一瞬间……」作为情绪升华的唯一收尾手法，全章最多用 1 次。",
        "**内心独白膨胀（禁止连续排比）**：人物内心独白（含括号标注的心理活动）若超过 3 行，必须在其中插入至少 1 个外部动作或对白节点打断；禁止用「他想，……他想，……他又想……」结构连续堆叠三层以上。",
        "**故事单元底线**：全章必须让读者看见目标、阻碍、行动、兑现、收获/代价、新期待中的至少 5 项；缺目标、缺阻碍、缺收获反馈时，不要靠文笔补救，要回到事件本身补一笔可见变化。",
    ]
    if beat_mode and beat_target_words is not None and beat_target_words <= 900:
        lines.extend(
            [
                "",
                "**节拍**：本段目标字数偏紧，优先完成大纲里的动作与关键对白；纠正式排比与内心铺陈从简。",
            ]
        )
    return "\n".join(lines)
